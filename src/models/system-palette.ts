export interface SystemPalette {

    /**
     * refers the border of the windows
     */
    windowBorder: string;

    /**
     * refers the background of windows
     */
    windowBackground: string;

    /**
     * color of text
     */
    textColor: string;

    /**
     * background color of appbar
     */
    appbarBackground: string;

    /**
     * background color of system header
     */
    headerBackground: string;
}
